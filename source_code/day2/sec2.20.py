# Addition
3 + 5

# Subtraction
7 - 3

# Multiplication
3 * 2

# Division
6 / 3

# Exponent, raising the number to the power of
2 ** 3

# PEMDAS, order of operations from left to right: () ** * / + -
# Parentheses       ()
# Exponent          **
# Multiplication    *
# Division          /
# Addition          +
# Subtractions      -

# Results = 7.0
print(3 * 3 + 3 / 3 - 3)

# Results = 3.0
print(3 * (3 + 3) / 3 - 3)